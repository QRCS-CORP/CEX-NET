                        Array.Clear(_state, 0, _state.Length);
                        _state = null;
                    }
                    if (_buffer != null)
                    {
                        Array.Clear(_buffer, 0, _buffer.Length);
                        _buffer = null;
                    }
                }
                finally
                {
                    _isDisposed = true;
                }
            }
        }
        #endregion
    }
