        #region Constructor
        /// <summary>
        /// Initialize the digest
        /// </summary>
        /// 
        /// <param name="DigestSize">Digest return size in bits</param>
        public Keccak256(int DigestSize = 256)
        {
            // test for legal sizes; default at 256
            if (DigestSize == 224)
                m_digestSize = 224 / 8;
            else if (DigestSize == 288)
                m_digestSize = 288 / 8;
            else
                m_digestSize = 256 / 8;

            m_blockSize = 200 - (m_digestSize * 2); //32=136
            Initialize();
        }

