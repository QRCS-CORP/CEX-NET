        #region Public Methods
        /// <summary>
        /// Update the SHA3 buffer
        /// </summary>
        /// 
        /// <param name="Input">Input data</param>
        /// <param name="InOffset">Offset within Input array</param>
        /// <param name="Length">Amount of data to process in bytes</param>
        /// 
        /// <exception cref="CryptoHashException">Thrown if an invalid Input size is chosen</exception>
        public void BlockUpdate(byte[] Input, int InOffset, int Length)
        {
            if ((InOffset + Length) > Input.Length)
                throw new CryptoHashException("Keccak256:BlockUpdate", "The Input buffer is too short!", new ArgumentOutOfRangeException());

            if (m_bufferIndex != 0)
            {
                if (Length + m_bufferIndex >= m_blockSize)
                {
                    int chunkSize = m_blockSize - m_bufferIndex;
                    Buffer.BlockCopy(Input, InOffset, m_buffer, m_bufferIndex, chunkSize);
                    TransformBlock(m_buffer, 0);
                    Length -= chunkSize;
                    InOffset += chunkSize;
                    m_bufferIndex = 0;
                }
            }

            while (Length >= m_buffer.Length)
            {
                TransformBlock(Input, InOffset);
                InOffset += m_buffer.Length;
                Length -= m_buffer.Length;
            }

            if (Length > 0)
            {
                Buffer.BlockCopy(Input, InOffset, m_buffer, m_bufferIndex, Length);
                m_bufferIndex += Length;
            }
        }

        /// <summary>
        /// Get the Hash value
        /// </summary>
        /// 
        /// <param name="Input">Input data</param>
        /// 
        /// <returns>Hash value</returns>
        public byte[] ComputeHash(byte[] Input)
        {
            byte[] hash = new byte[DigestSize];

            BlockUpdate(Input, 0, Input.Length);
            DoFinal(hash, 0);

            return hash;
        }

        /// <summary>
        /// Do final processing and get the hash value
        /// </summary>
        /// 
        /// <param name="Output">The Hash value container</param>
        /// <param name="OutOffset">The starting offset within the Output array</param>
        /// 
        /// <returns>Size of Hash value</returns>
        /// 
        /// <exception cref="CryptoHashException">Thrown if Output array is too small</exception>
        public int DoFinal(byte[] Output, int OutOffset)
        {
            if (Output.Length - OutOffset < DigestSize)
                throw new CryptoHashException("Keccak256:DoFinal", "The Output buffer is too short!", new ArgumentOutOfRangeException());

            Array.Clear(m_buffer, m_bufferIndex, m_buffer.Length - m_bufferIndex); 
            m_buffer[m_bufferIndex] = 1;
            m_buffer[BlockSize - 1] |= 128;

            TransformBlock(m_buffer, 0);

            m_state[1] = ~m_state[1];
            m_state[2] = ~m_state[2];
            m_state[8] = ~m_state[8];
            m_state[12] = ~m_state[12];
            m_state[17] = ~m_state[17];

            Buffer.BlockCopy(ULongsToBytes(m_state, 0, m_state.Length), 0, Output, OutOffset, m_digestSize);
            Initialize();

            return DigestSize;
        }

        /// <summary>
        /// Reset the internal state
        /// </summary>
        public void Reset()
        {
            Initialize();
        }

        /// <summary>
        /// Update the digest with a single byte
        /// </summary>
        /// 
        /// <param name="Input">Input byte</param>
        public void Update(byte Input)
        {
            BlockUpdate(new byte[] { Input }, 0, 1);
        }
